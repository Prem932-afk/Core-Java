
public class PrintAlphabets 
{
    public static void main(String[] args) 
    {
        // Initialize a char variable to 'a'
        char ch = 'a';

        // While loop to print alphabets from 'a' to 'z'
        while (ch <= 'z') 
        {
            System.out.print(ch + " ");
            ch++; // Move to the next alphabet
        }
    }
}
