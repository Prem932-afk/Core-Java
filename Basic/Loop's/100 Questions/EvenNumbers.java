
public class EvenNumbers 
{
    public static void main(String[] args) 
    {
        // Initialize the number to 2, as it's the first even number
        int num = 2;

        // While loop to print even numbers between 1 and 100
        while (num <= 100) 
        {
            System.out.print(num + " ");
            num += 2; // Increment by 2 to get the next even number
        }
    }
}
