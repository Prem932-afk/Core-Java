//Q.14 Average total marks?
public class P14 
{
  public static void main(String[] args) 
  {
     int maths=65;
     int eng=77;
     int hindi=56;
     int science=44;
     int total = maths + eng + hindi + science;
     System.out.println("Total Marks :" + total);
     
     int Average= total / 4;
     System.out.println("Average marks :" + Average);
  }    
}
